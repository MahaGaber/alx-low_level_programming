#include"3-calc.h"

/**
 * op_add - it add
 * @a: integer
 * @b:integer
 *Return:integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - it subtract
 * @a: integer
 * @b:integer
 *Return:integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - it add
 * @a: integer
 * @b:integer
 *Return:integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - it divide
 * @a: integer
 * @b:integer
 *Return:integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - it add
 * @a: integer
 * @b:integer
 *Return:integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}


